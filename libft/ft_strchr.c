#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i])
	{
		if (s[i] == (unsigned char)c)
			return (&((char *)s)[i]);
		i++;
	}
	if (s[i] == '\0' && (unsigned char)c == '\0')
		return (&((char *)s)[i]);
	else
		return ((void *) NULL);
}
