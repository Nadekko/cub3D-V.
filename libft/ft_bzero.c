#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	if (!s || !n)
		return ;
	str = (char *)s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
}
